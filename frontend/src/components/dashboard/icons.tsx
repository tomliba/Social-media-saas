// Inline SVG icons for nav controls.
//
// These use inline SVG instead of the Material Symbols icon font because the
// font is subset at build time to only the glyphs already in use; a newly
// referenced font glyph falls back to raw ligature text ("menu", "logout")
// until the font is re-subset. Inline SVG always renders — same rationale as
// components/ui/Spinner.tsx. Stroke uses currentColor so callers control color.

export function MenuIcon({ className }: { className?: string }) {
  return (
    <svg
      viewBox="0 0 24 24"
      width="24"
      height="24"
      fill="none"
      stroke="currentColor"
      strokeWidth="2"
      strokeLinecap="round"
      className={className}
      aria-hidden="true"
    >
      <line x1="3" y1="6" x2="21" y2="6" />
      <line x1="3" y1="12" x2="21" y2="12" />
      <line x1="3" y1="18" x2="21" y2="18" />
    </svg>
  );
}

